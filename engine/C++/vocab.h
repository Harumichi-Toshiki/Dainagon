    static int POLICY_INDEX_TABLE[16384];

    // Move構造体から、一意の整数IDを計算する
    inline int get_move_id(const Move& m) {
        if (m.is_drop) {
            return 13122 + m.type * 81 + m.to;
        }
        else {
            return m.from * 162 + m.to * 2 + (m.is_promote ? 1 : 0);
        }
    }

    // 起動時に1回だけ呼ばれる初期化関数
    inline void init_vocab() {
        for (int i = 0; i < 16384; ++i) POLICY_INDEX_TABLE[i] = -1;
        for (int i = 0; i < (int)USI_MOVES.size(); ++i) {
            const std::string& usi = USI_MOVES[i];
            if (usi == "pass" || usi == "resign") continue;
            Move m;
            m.is_promote = (usi.back() == '+');
            if (usi[1] == '*') {
                m.is_drop = true; m.from = -1;
                switch (usi[0]) {
                case 'P': m.type = PAWN; break; case 'L': m.type = LANCE; break;
                case 'N': m.type = KNIGHT; break; case 'S': m.type = SILVER; break;
                case 'G': m.type = GOLD; break; case 'B': m.type = BISHOP; break; case 'R': m.type = ROOK; break;
                }
                m.to = (usi[3] - 'a') * 9 + ('9' - usi[2]);
            }
            else {
                m.is_drop = false;
                m.from = (usi[1] - 'a') * 9 + ('9' - usi[0]);
                m.to = (usi[3] - 'a') * 9 + ('9' - usi[2]);
                m.type = 0;
            }
            POLICY_INDEX_TABLE[get_move_id(m)] = i;
        }
    }
